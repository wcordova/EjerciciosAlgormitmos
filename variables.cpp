#include <iostream>

int main() {
    // Declaración e inicialización de variables
    int edad = 25;             // Variable de tipo entero
    double salario = 3500.75;  // Variable de tipo double (número decimal)
    char inicial = 'hola';        // Variable de tipo carácter
    
    edad = 'hola';

    // Imprimir los valores de las variables
    std::cout << "Edad: " << edad << std::endl;
    std::cout << "Salario: " << salario << std::endl;
    std::cout << "Inicial: " << inicial << std::endl;

    return 0;
}

